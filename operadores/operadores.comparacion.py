# OPERADORES COMPARACION

num1 = 5
num2 = 2

# igual
print(num1 == num2) #5 es igual a 2 /False

# diferente
print(num1 != num2) #5 es diferente a 2 /True

# menor
print(num1 < num2) #5 es menor que 2 /False

# menor igual
print(num1 <= num2) #5 es menor igual a 2 /False

# mayor
print(num1 > num2) #5 es mayor que 2 /True

# mayor igual
print(num1 >= num2) #5 es mayor igual a 2 /True