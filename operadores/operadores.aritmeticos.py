# OPERADORES ARTIMETICOS

num1 = 5
num2 = 2

# suma
print(num1 + num2)

# resta
print(num1 - num2)

# multipliacion
print(num1 * num2)

# divicion
print(num1 / num2)

# modulo
print(num1 % num2)

# potenciacion
print(num1 ** num2)

# division baja: devuelve el entero redondeado hacia a abajo: 2.5 -> 2
print(num1 // num2)